public class ArrayExample
{
    /// <summary>
    /// Arrays in C#
    /// 
    /// What is an Array?
    /// - An Array is a collection of elements of the same type stored in a **contiguous memory space** (unbreaking memory space).
    /// - This structure allows for fast indexing, as each element's memory address is calculated based on its index.
    /// 
    /// Key Features:
    /// - Fixed Size: The size of an array is determined when it is created and cannot be changed.
    /// - Contiguous Memory: All elements are stored together in memory, making it efficient to traverse.
    /// - Zero-Based Indexing: Arrays in C# start indexing at 0.
    /// 
    /// Example:
    /// - If you have an array of integers like `int[] numbers = { 1, 2, 3 };`, the elements are stored contiguously in memory.
    /// - Each element can be accessed directly using its index, e.g., `numbers[0]` for the first element or `numbers[2]` for the third.
    /// 
    /// Why use Arrays?
    /// - Arrays are simple, lightweight, and efficient for accessing elements when the size of the collection is known and fixed.
    /// - They are foundational data structures used in many programming scenarios.
    /// </summary>
}